# Convert a given binary tree into a doubly-linked list in-place. The left and right pointers of the tree's nodes are to be used as the previous and next pointers of the list's nodes, respectively. The order of the nodes in the doubly-linked list must correspond to the in-order traversal of the tree. The head of the doubly-linked list must point to the first node visted in an in-order traversal.

# Time: O(n)
# Space: O(1)

# Example:

# Input:

#         10
#       /    \
#     12      15
#     /\      /
#   25  30   36

# Output:

# head
#    v
#    25 <-> 12 <-> 30 <-> 10 <-> 36 <-> 15


